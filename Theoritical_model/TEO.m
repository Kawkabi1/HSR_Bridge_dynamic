function [U1]=TEO(EI,L,m,df,v,p)
%_______________________________________________________________________%
%                              INPUTS                                   %
%_______________________________________________________________________%
syms n
x=L/2;
U=[];
i=0;
t=0:0.01:L/v;
%_______________________________________________________________________%
%                            CALCULATIONS                               %
%_______________________________________________________________________%
U1=0;
U2=0;
for n=1:1
ef=(n*pi*v)/L;
omega_n=(((n^2)*pi^2)/L^2)*sqrt((EI)/m);
S_n=ef/omega_n;
omega_nd=omega_n*sqrt(1-(df^2));
%_______________________________________________________________________%
A=(1-S_n^2)*sin(ef*t)-2*df*S_n*cos(ef*t)+exp(-df*omega_n*t);
A1=(1-S_n^2)*sin(ef*t)+1;
B=2*df*S_n*cos(omega_nd*t)+(S_n/sqrt(1-df^2))*(2*(df^2)+(S_n^2)-1)*sin(omega_nd*t);
B1=(S_n)*((S_n)-1)*sin(omega_nd*t);
C=sin((n*pi*x)/L);
D=(2*p*(L^3))/(EI*n^4*pi^4);
E=(1-(S_n^2))^2+(2*df*S_n)^2;
E1=(1-S_n)^2;
U1=U1+1000*(D/E).*A.*B.*C;
U2=U2+1000*(D/E1).*A1.*B1.*C;
%U21=symsum(U2,n,1,10)
end
%plot(t,U1)
%hold on 
